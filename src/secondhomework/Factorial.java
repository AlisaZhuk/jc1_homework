package secondhomework;

    public class Factorial {
            public static void main(String[] args) {
                long fact = 0;
                if (fact == 0);
                {
                    fact = 1;
                    System.out.println(fact);
                }
                int i = 1;
                while (i <= 10){
                    fact = fact * i;
                    i++;
                    System.out.println(fact);
                }

            }
    }
